  puts "sum"
  sum = num1 + num2
end

def subtraction(num1, num2)
  puts "minus"
  minus = num1 - num2
end

def division(num1, num2)
  puts "divide"
  divide = num1 / num2
end

def multiplication(num1, num2)
  puts "multiply"
  multiply = num1 * num2
end

